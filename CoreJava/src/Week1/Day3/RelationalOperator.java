package Day3;

public class RelationalOperator {
    public static void main(String[] args) {
        //comparison operator
        //used to compare two values or expression and determine their relationship or order
        //>,<,==,!=,>=,<=
        int a=12;
        int b=13;
        if(a<b){
            System.out.println(a);
        }
        else {
            System.out.println(b);
        }
    }
}
