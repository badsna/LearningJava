package Day3;

public class ArithmeticOperator {
    public static void main(String[] args) {
        // used to perform common mathematical operations(+,-,/,*,%)
        int a=5;
        int b=4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(--b);
        System.out.println(b--);
    }

}
